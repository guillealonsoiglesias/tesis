def _softmax(x: np.ndarray) -> np.ndarray:
    """
    Softmax fila a fila sobre un array 2D.

    Si se pasa un vector 1D, se convierte internamente en 2D (shape (1, n)).
    """
    x = np.asarray(x, dtype=float)
    if x.ndim == 1:
        x = x.reshape(1, -1)
    x_max = np.max(x, axis=1, keepdims=True)
    e = np.exp(x - x_max)
    s = e.sum(axis=1, keepdims=True)
    # Evitar divisiones por cero
    s[s == 0.0] = 1.0
    return e / s


def _ensure_2d_array(X: Any) -> np.ndarray:
    """
    Asegura que la entrada se convierte en un array 2D de tipo float.

    - Si es un DataFrame, usa .to_numpy().
    - Si es un vector 1D, lo convierte en shape (1, n_features).
    """
    if isinstance(X, pd.DataFrame):
        A = X.to_numpy(dtype=float)
    else:
        A = np.asarray(X, dtype=float)
    if A.ndim == 1:
        A = A.reshape(1, -1)
    return A


def _is_int_string(s: str) -> bool:
    """Devuelve True si la cadena representa un entero (signo opcional)."""
    if s is None:
        return False
    s = str(s).strip()
    if not s:
        return False
    if s[0] in "+-":
        s = s[1:]
    return s.isdigit()

