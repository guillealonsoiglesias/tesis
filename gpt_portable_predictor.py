import json

def build_prediction_payload(
    predictor: PortablePredictor,
    df_raw: pd.DataFrame,
    row_index: int = 0,
    knn_topk: int = 10,
    knn_space: str = "all",
) -> Dict[str, Any]:
    """
    Construye un diccionario (JSON-serializable) con toda la información
    relevante para un único expediente.

    Pensado para que un GPT (con Python) pueda:
      - ejecutar esta función,
      - obtener el dict,
      - y a partir de ahí generar una explicación en lenguaje natural.

    Parámetros
    ----------
    predictor : PortablePredictor
        Instancia ya cargada con load_predictor_from_zip().
    df_raw : DataFrame
        DataFrame con al menos una fila, en el formato esperado por predictor.
        (idealmente, df_norm tras export_python_transformacion).
    row_index : int
        Índice posicional (iloc) de la fila a explicar.
    knn_topk : int
        Número de vecinos más cercanos a devolver.
    knn_space : {"train","all"}
        Conjunto de referencia para el kNN.

    Devuelve
    --------
    dict
        Objeto listo para pasarse a json.dumps().
    """
    if not isinstance(df_raw, pd.DataFrame):
        raise TypeError("build_prediction_payload espera un pandas.DataFrame en df_raw.")

    if df_raw.shape[0] == 0:
        raise ValueError("df_raw no contiene filas.")

    if row_index < 0 or row_index >= df_raw.shape[0]:
        raise IndexError(f"row_index={row_index} fuera de rango para df_raw con {df_raw.shape[0]} filas.")

    # Extraemos una sola fila como DataFrame (preserva nombres de columnas)
    df_one = df_raw.iloc[[row_index]].copy()
    idx_label = df_one.index[0]

    # 1) Predicciones de todos los modelos
    res = predictor.predict(df_one)

    ensemble_df = res["ensemble"]
    rf_base_out = res["rf_base"]
    rf_124_out = res["rf_124"]
    xgb_out = res["xgb_baja"]

    # Fila única del ensemble
    ens_row = ensemble_df.loc[idx_label]

    # RF base y RF_124: proba de la fila
    rf_base_proba_row = rf_base_out["proba"].loc[idx_label]
    rf_124_proba_row = rf_124_out["proba"].loc[idx_label]

    # 2) Vecinos kNN para esa misma fila
    knn_res = predictor.knn_neighbors(df_one, topk=knn_topk, space=knn_space)
    knn_indices = knn_res["indices"][0].tolist()
    knn_distances = [float(d) for d in knn_res["distances"][0]]
    knn_similarity = [float(s) for s in knn_res["similarity_percent"][0]]
    p95_nn1_train = float(knn_res["p95_nn1_train"])

    # 3) Entrada "cruda": valores de la fila en df_raw (ya normalizado)
    input_row = df_one.iloc[0].to_dict()
    # Convertimos posibles tipos numpy a tipos nativos
    input_row_clean = {}
    for k, v in input_row.items():
        if isinstance(v, (np.generic, np.bool_)):
            input_row_clean[k] = v.item()
        else:
            input_row_clean[k] = v

    # 4) Construimos el payload
    payload = {
        "input": {
            "index_label": str(idx_label),
            "row_index": int(row_index),
            "features": input_row_clean,
        },
        "models": {
            "xgb_baja": {
                "p_bajo": float(xgb_out["p_bajo"].loc[idx_label]),
                "baja_comp": int(xgb_out["baja_comp"].loc[idx_label]),
                "threshold_bajo": float(xgb_out["threshold_bajo"]),
                "labels": list(predictor.xgb_baja.labels),
            },
            "rf_base": {
                "pred": str(rf_base_out["pred"].loc[idx_label]),
                "proba": {
                    str(cls): float(rf_base_proba_row[cls])
                    for cls in predictor.rf_base.classes_
                },
                "classes": [str(c) for c in predictor.rf_base.classes_],
            },
            "rf_124": {
                "pred": str(rf_124_out["pred"].loc[idx_label]),
                "proba": {
                    str(cls): float(rf_124_proba_row[cls])
                    for cls in predictor.rf_124.classes_
                },
                "classes": [str(c) for c in predictor.rf_124.classes_],
            },
        },
        "ensemble": {
            "pred_final": str(ens_row["pred"]),
            "confianza_pred": float(ens_row["confianza_pred"]),
            "fuente_confianza": str(ens_row["fuente_confianza"]),
            "flip_ensemble": bool(ens_row["flip_ensemble"]),
            "baja_comp": int(ens_row["baja_comp"]),
            "p_bajo": float(ens_row["p_bajo"]),
        },
        "knn": {
            "space": str(knn_space),
            "topk": int(knn_topk),
            "indices": [int(i) for i in knn_indices],
            "distances": knn_distances,
            "similarity_percent": knn_similarity,
            "p95_nn1_train": p95_nn1_train,
        },
        "meta": {
            "zip_path": str(predictor.zip_path),
            "notes": (
                "Probabilidades RF portables pueden diferir hasta ~0.02 respecto "
                "a sklearn; threshold_bajo del XGB se fija explícitamente en 0.38."
            ),
        },
    }

    return payload


