class KNNPortable:
    """
    Implementación portable del espacio kNN PRE_NUM.

    Se basa en:
      - knn_space_portable.json
      - knn_train.npz (Z_train)
      - knn_all.npz   (Z_all)

    Funcionalidad:
      - Proyectar nuevas filas al espacio PRE_NUM con el pipeline numérico
        (imputación + escalado).
      - Calcular distancias euclídeas a Z_train o Z_all.
      - Devolver índices y distancias de los k vecinos, junto con una
        medida de similitud (%) basada en p95_nn1_train.
    """

    def __init__(
        self,
        meta: Dict[str, Any],
        Z_train: np.ndarray,
        Z_all: np.ndarray,
    ) -> None:
        self.meta = meta
        self.pre_num_features: List[str] = list(meta.get("pre_num_features", []))
        self.metric: str = str(meta.get("metric", "euclidean"))
        self.k_neighbors: int = int(meta.get("k_neighbors", 25))
        self.p95_nn1_train: float = float(meta.get("p95_nn1_train", 0.0))

        num_pipeline = meta.get("num_pipeline", {}) or {}
        num_imp = num_pipeline.get("imputer", {}) or {}
        num_scl = num_pipeline.get("scaler", {}) or {}

        self.imp_strategy: str = num_imp.get("strategy", "median")
        self.imp_statistics: np.ndarray = np.asarray(
            num_imp.get("statistics", []), dtype=float
        )

        self.with_mean: bool = bool(num_scl.get("with_mean", True))
        self.with_std: bool = bool(num_scl.get("with_std", True))
        self.mean_: np.ndarray = np.asarray(
            num_scl.get("mean", np.zeros(len(self.pre_num_features))), dtype=float
        )
        self.scale_: np.ndarray = np.asarray(
            num_scl.get("scale", np.ones(len(self.pre_num_features))), dtype=float
        )
        self.scale_[self.scale_ == 0.0] = 1.0  # evitar divisiones por cero

        # Catálogo (metadatos informativos; no podemos reconstruir el DF desde aquí)
        self.catalog_meta: Dict[str, Any] = meta.get("catalog", {}) or {}

        # Matrices de referencia
        self.Z_train: np.ndarray = np.asarray(Z_train, dtype=float)
        self.Z_all: np.ndarray = np.asarray(Z_all, dtype=float)

        if self.Z_train.ndim != 2 or self.Z_all.ndim != 2:
            raise ValueError("Z_train y Z_all deben ser matrices 2D.")

    # -------------------- proyección al espacio PRE_NUM --------------------

    def _build_X_num(self, df: pd.DataFrame) -> pd.DataFrame:
        """
        Construye el DataFrame numérico X_num con las columnas pre_num_features
        en el orden exacto, aplicando algunas reglas de fallback coherentes
        con el pipeline original (C_precio_p <- Criterio_precio_p).
        """
        if not isinstance(df, pd.DataFrame):
            raise TypeError("KNNPortable espera un pandas.DataFrame como entrada.")

        X_num = pd.DataFrame(index=df.index)

        for c in self.pre_num_features:
            if c in df.columns:
                X_num[c] = pd.to_numeric(df[c], errors="coerce")
            elif c == "C_precio_p" and "Criterio_precio_p" in df.columns:
                # Fallback: usar la columna original de criterio si existe
                X_num[c] = pd.to_numeric(df["Criterio_precio_p"], errors="coerce")
            else:
                X_num[c] = np.nan

        return X_num[self.pre_num_features]

    def _transform(self, df: pd.DataFrame) -> np.ndarray:
        """
        Aplica el imputer + scaler numérico del pipeline kNN a df, y devuelve
        la matriz NumPy 2D en el espacio PRE_NUM.
        """
        X_num = self._build_X_num(df)
        A = X_num.to_numpy(dtype=float)

        # Imputación por estadísticos
        if self.imp_strategy in ("median", "most_frequent"):
            stats = self.imp_statistics
            if stats.shape[0] != A.shape[1]:
                raise ValueError(
                    "Dimensión de estadísticas de imputación kNN inconsistente: "
                    f"{stats.shape[0]} != {A.shape[1]}"
                )
            for j in range(A.shape[1]):
                col = A[:, j]
                mask = np.isnan(col)
                if mask.any():
                    col[mask] = stats[j]

        # Escalado
        if self.with_mean:
            A = A - self.mean_
        if self.with_std:
            A = A / self.scale_

        return A

    # -------------------- distancias y vecinos --------------------

    def _compute_distances(self, Zq: np.ndarray, space: str) -> np.ndarray:
        """
        Calcula la matriz de distancias entre Zq (consultas) y Z_base
        (train o all) usando la métrica configurada.

        Actualmente solo se implementa 'euclidean'; otras métricas se
        degradan a euclidiana de forma explícita.
        """
        if space == "train":
            Z_base = self.Z_train
        elif space == "all":
            Z_base = self.Z_all
        else:
            raise ValueError("space debe ser 'train' o 'all'.")

        if self.metric not in ("euclidean", "l2"):
            # Degradamos a euclidiana, lo documentamos en el docstring
            pass

        # Broadcasting: (n_q, 1, d) - (1, n_ref, d) -> (n_q, n_ref, d)
        diff = Zq[:, None, :] - Z_base[None, :, :]
        dist = np.sqrt(np.sum(diff ** 2, axis=2))  # shape (n_q, n_ref)
        return dist

    def neighbors(
        self,
        df: pd.DataFrame,
        topk: int = 10,
        space: str = "all",
    ) -> Dict[str, Any]:
        """
        Calcula los k vecinos más cercanos para cada fila de df.

        Parámetros
        ----------
        df : DataFrame
            DataFrame con las variables PRE necesarias (al menos las que
            figuran en pre_num_features).
        topk : int, por defecto 10
            Número de vecinos a devolver (se trunca si hay menos filas
            en Z_base).
        space : {"train","all"}, por defecto "all"
            Conjunto de referencia (Z_train o Z_all).

        Devuelve
        --------
        dict con:
            - "indices" : np.ndarray (n_samples, k) de índices de vecinos.
            - "distances": np.ndarray (n_samples, k) de distancias euclídeas.
            - "similarity_percent": np.ndarray (n_samples, k) con sim% = max(0, 1 - d/p95)*100.
            - "p95_nn1_train": float (el mismo valor meta).
        """
        Zq = self._transform(df)
        Zq = _ensure_2d_array(Zq)

        dist = self._compute_distances(Zq, space=space)
        n_q, n_ref = dist.shape

        k = max(1, min(int(topk), n_ref))
        # argsort a lo largo del eje de referencia
        # índices de vecinos ordenados por distancia ascendente
        idx_sorted = np.argsort(dist, axis=1)[:, :k]

        # Distancias ordenadas
        rows = np.arange(n_q)[:, None]
        dist_sorted = dist[rows, idx_sorted]

        # Similitud porcentual basada en p95_nn1_train
        if self.p95_nn1_train > 0:
            sim_percent = np.maximum(
                0.0,
                1.0 - dist_sorted / float(self.p95_nn1_train),
            ) * 100.0
        else:
            sim_percent = np.zeros_like(dist_sorted)

        return {
            "indices": idx_sorted.astype(int),
            "distances": dist_sorted.astype(float),
            "similarity_percent": sim_percent.astype(float),
            "p95_nn1_train": float(self.p95_nn1_train),
        }

"""Celda 7 – EnsemblePredictor (nueva lógica RF base vs RF_124)"""

# ---------------------------------------------------------------------------
# EnsemblePredictor
# ---------------------------------------------------------------------------

class EnsemblePredictor:
    """
    Ensemble simplificado entre RF base y RF_124_BASE+BC.

    Lógica:
      - Tomamos RF base como predicción por defecto.
      - Solo activamos el ensemble cuando la clase top-1 del RF base está
        en classes_124 = {"1","2","4"}.
      - Además exigimos que la clase top-1 del RF_124 también esté en ese
        mismo conjunto.
      - En ese caso, comparamos la probabilidad top-1 (p1) de cada modelo
        y escogemos el modelo cuya p1 sea mayor.
      - En caso de empate, se mantiene la predicción del RF base.
      - margin_min y otras reglas "seguras" NO se aplican aquí.

    La salida incorpora también la información de baja_comp del modelo XGB.
    """

    def __init__(self, classes_124: Optional[List[str]] = None) -> None:
        if classes_124 is None:
            classes_124 = ["1", "2", "4"]
        # Guardamos el conjunto de clases donde se considera el especialista
        self.classes_124 = set(str(c) for c in classes_124)

    # -------------------- utilidades internas --------------------

    @staticmethod
    def _top1_top2(row: np.ndarray, labels: List[str]) -> Tuple[str, float, float]:
        """
        Dada una fila de probabilidades y la lista de labels,
        devuelve (label_top1, p1, p2), donde p2 es la segunda probabilidad
        más alta (útil si quisieras volver a un criterio por margen).
        """
        row = np.asarray(row, dtype=float)
        if row.ndim != 1:
            raise ValueError("_top1_top2 espera un vector 1D de probabilidades.")

        # Ordenamos índices por probabilidad descendente
        idx_sorted = np.argsort(row)[::-1]
        idx1 = idx_sorted[0]
        p1 = float(row[idx1])

        if len(idx_sorted) > 1:
            idx2 = idx_sorted[1]
            p2 = float(row[idx2])
        else:
            p2 = 0.0

        label_top1 = str(labels[idx1])
        return label_top1, p1, p2

    # -------------------- API principal --------------------

    def apply(
        self,
        pred_base: Any,
        proba_base: Any,
        labels_base: List[str],
        pred_124: Any,
        proba_124: Any,
        labels_124: List[str],
        baja_comp: pd.Series,
        p_bajo: pd.Series,
    ) -> pd.DataFrame:
        """
        Aplica la lógica de ensemble sobre las predicciones de RF base,
        RF_124 y XGB baja.

        Parámetros
        ----------
        pred_base : array-like
            Clases predichas por RF base (no se usan directamente aquí,
            se recalculan desde las probabilidades).
        proba_base : array-like (n_samples, n_classes_base)
            Probabilidades de RF base.
        labels_base : list[str]
            Etiquetas de las clases de RF base en el mismo orden que las columnas
            de proba_base.
        pred_124 : array-like
            Clases predichas por RF_124 (no se usan directamente aquí).
        proba_124 : array-like (n_samples, n_classes_124)
            Probabilidades de RF_124.
        labels_124 : list[str]
            Etiquetas de las clases de RF_124 en el mismo orden que las columnas
            de proba_124.
        baja_comp : pd.Series
            Indicador 0/1 de baja_comp procedente de XGB.
        p_bajo : pd.Series
            Probabilidad de clase BAJO procedente de XGB.

        Devuelve
        --------
        DataFrame con columnas:
          - 'pred'             : clase final (str)
          - 'confianza_pred'   : probabilidad top-1 del modelo elegido
          - 'fuente_confianza' : 'rf_base' o 'rf_124_basebc'
          - 'flip_ensemble'    : bool, True si se cambió desde rf_base a rf_124
          - 'baja_comp'        : 0/1
          - 'p_bajo'           : probabilidad de BAJO
        """
        # Convertimos a arrays NumPy
        proba_base = np.asarray(proba_base, dtype=float)
        proba_124 = np.asarray(proba_124, dtype=float)

        n = proba_base.shape[0]
        if proba_124.shape[0] != n:
            raise ValueError("proba_base y proba_124 tienen número de filas distinto.")

        # Aseguramos que baja_comp y p_bajo tienen longitud compatible
        if len(baja_comp) != n or len(p_bajo) != n:
            raise ValueError(
                "baja_comp y p_bajo deben tener la misma longitud que las matrices de probas."
            )

        # Index para el DataFrame de salida
        idx_out = baja_comp.index

        # Inicializamos salidas
        pred_final = np.empty(n, dtype=object)
        fuente = np.full(n, "rf_base", dtype=object)
        conf_final = np.zeros(n, dtype=float)
        flip = np.zeros(n, dtype=bool)

        # Recorremos fila a fila
        for i in range(n):
            row_base = proba_base[i]
            row_124 = proba_124[i]

            base_label_top1, p1_base, _ = self._top1_top2(row_base, labels_base)
            spec_label_top1, p1_spec, _ = self._top1_top2(row_124, labels_124)

            # Por defecto, siempre usamos RF base
            pred_final[i] = base_label_top1
            conf_final[i] = p1_base
            fuente[i] = "rf_base"

            # Solo activamos ensemble si la clase base está en {1,2,4}
            if base_label_top1 not in self.classes_124:
                continue

            # Y si RF_124 también predice una clase válida del especialista
            if spec_label_top1 not in self.classes_124:
                continue

            # Regla de decisión: elegir el modelo con mayor probabilidad top-1
            if p1_spec > p1_base:
                pred_final[i] = spec_label_top1
                conf_final[i] = p1_spec
                fuente[i] = "rf_124_basebc"
                flip[i] = True
            # Si p1_spec <= p1_base, nos quedamos con la predicción base

        df_out = pd.DataFrame(
            {
                "pred": pred_final.astype(str),
                "confianza_pred": conf_final.astype(float),
                "fuente_confianza": fuente.astype(str),
                "flip_ensemble": flip.astype(bool),
                "baja_comp": baja_comp.astype(int).values,
                "p_bajo": p_bajo.astype(float).values,
            },
            index=idx_out,
        )

        return df_out

"""Celda 8 – PortablePredictor + load_predictor_from_zip (API de alto nivel)"""

# ---------------------------------------------------------------------------
# PortablePredictor: fachada principal
# ---------------------------------------------------------------------------

class PortablePredictor:
    """
    Fachada principal para usar todos los modelos portables desde un ZIP.

    Flujo típico de uso:
      predictor = PortablePredictor.from_zip("modelos_portable.zip")
      resultados = predictor.predict(df_norm)
      vecinos = predictor.knn_neighbors(df_norm.iloc[[0]], topk=10, space="all")

    Importante:
      - Este predictor asume que df_norm (df_raw en las firmas) ya está en el
        formato "normalizado" que consumen los modelos:
          * columnas y tipos compatibles con expected_raw_features de los RF
          * variables_predictoras del XGB de baja_comp
          * pre_num_features del espacio kNN
        Es decir, df_norm debería ser la salida del script de transformación
        (export_python_transformacion.py) aplicada sobre los datos originales.
    """

    def __init__(
        self,
        zip_path: str,
        store: ZipStore,
        rf_base: RandomForestPortable,
        rf_124: RandomForestPortable,
        xgb_baja: XGBBajaPortable,
        knn: KNNPortable,
        ensemble: EnsemblePredictor,
        baja_comp_dup_times: int,
        ensemble_meta: Dict[str, Any],
        preprocess_pre: Optional[PortablePreprocessor] = None,
        centroids_pre: Optional[Dict[str, Any]] = None,
    ) -> None:
        self.zip_path = zip_path
        self.store = store
        self.rf_base = rf_base
        self.rf_124 = rf_124
        self.xgb_baja = xgb_baja
        self.knn = knn
        self.ensemble = ensemble
        self.baja_comp_dup_times = int(baja_comp_dup_times)
        self.ensemble_meta = ensemble_meta
        self.preprocess_pre = preprocess_pre
        self.centroids_pre = centroids_pre

    # -------------------- construcción desde ZIP --------------------

    @classmethod
    def from_zip(cls, zip_path: str) -> "PortablePredictor":
        """
        Carga todos los artefactos portables desde un ZIP y construye
        un PortablePredictor listo para usar.

        Parámetros
        ----------
        zip_path : str
            Ruta al fichero modelos_portable.zip.

        Devuelve
        --------
        PortablePredictor
        """
        store = ZipStore(zip_path)

        # 1) Meta del ensemble (punto de entrada común)
        if not store.has("ensemble_portable.json"):
            raise FileNotFoundError(
                "No se ha encontrado 'ensemble_portable.json' en el ZIP."
            )
        ensemble_meta = store.read_json("ensemble_portable.json")

        deps = ensemble_meta.get("depends_on_portable", {}) or {}

        # 2) Artefactos RF base y RF_124
        rf_base_name = deps.get("rf_base", "rf_base_portable.json")
        rf_124_name = deps.get("rf_124_basebc", "rf_124_portable.json")

        if not store.has(rf_base_name):
            raise FileNotFoundError(f"Falta artefacto RF base: {rf_base_name!r}")
        if not store.has(rf_124_name):
            raise FileNotFoundError(f"Falta artefacto RF_124: {rf_124_name!r}")

        rf_base_art = store.read_json(rf_base_name)
        rf_124_art = store.read_json(rf_124_name)

        rf_base = RandomForestPortable(rf_base_art)
        rf_124 = RandomForestPortable(rf_124_art)

        # 3) Artefacto XGB baja_comp (meta + booster)
        xgb_meta_name = deps.get("xgb_baja", "xgb_baja_portable.json")
        if not store.has(xgb_meta_name):
            raise FileNotFoundError(f"Falta artefacto XGB meta: {xgb_meta_name!r}")
        xgb_meta = store.read_json(xgb_meta_name)

        # Booster guardado con Booster.save_model en formato JSON
        if not store.has("xgb_baja_model.json"):
            raise FileNotFoundError("Falta 'xgb_baja_model.json' en el ZIP.")
        xgb_booster = store.read_json("xgb_baja_model.json")

        xgb_baja = XGBBajaPortable(xgb_meta, xgb_booster)

        # 4) Espacio kNN PRE_NUM
        knn_meta_name = deps.get("knn_space", "knn_space_portable.json")
        if not store.has(knn_meta_name):
            raise FileNotFoundError(f"Falta artefacto kNN meta: {knn_meta_name!r}")
        knn_meta = store.read_json(knn_meta_name)

        if not store.has("knn_train.npz"):
            raise FileNotFoundError("Falta 'knn_train.npz' en el ZIP.")
        if not store.has("knn_all.npz"):
            raise FileNotFoundError("Falta 'knn_all.npz' en el ZIP.")

        Z_train_npz = store.read_npz("knn_train.npz")
        Z_all_npz = store.read_npz("knn_all.npz")

        if "Z" not in Z_train_npz.files:
            raise KeyError("En 'knn_train.npz' se esperaba un array 'Z'.")
        if "Z" not in Z_all_npz.files:
            raise KeyError("En 'knn_all.npz' se esperaba un array 'Z'.")

        knn = KNNPortable(
            meta=knn_meta,
            Z_train=Z_train_npz["Z"],
            Z_all=Z_all_npz["Z"],
        )

        # 5) Preprocesador PRE global (opcional, por si quieres usarlo fuera)
        preprocess_pre = None
        centroids_pre = None
        pre_pre_name = deps.get("preprocess_pre", "preprocess_pre_portable.json")
        if store.has(pre_pre_name):
            pre_pre_art = store.read_json(pre_pre_name)
            # El esquema de preprocess_pre es compatible con PortablePreprocessor
            prep_schema = {
                "numeric": pre_pre_art.get("numeric", {}),
                "categorical": pre_pre_art.get("categorical", {}),
                "feature_names_out": pre_pre_art.get("feature_names_out"),
            }
            preprocess_pre = PortablePreprocessor(prep_schema)
            centroids_pre = pre_pre_art.get("centroids_ref") or pre_pre_art.get("centroids")

        # 6) Ensemble
        ensemble = EnsemblePredictor(classes_124=["1", "2", "4"])

        # 7) Configuración de baja_comp duplicada para RF_124
        baja_cfg = ensemble_meta.get("baja_comp", {}) or {}
        baja_comp_dup_times = int(baja_cfg.get("duplicated_times", 5))

        return cls(
            zip_path=zip_path,
            store=store,
            rf_base=rf_base,
            rf_124=rf_124,
            xgb_baja=xgb_baja,
            knn=knn,
            ensemble=ensemble,
            baja_comp_dup_times=baja_comp_dup_times,
            ensemble_meta=ensemble_meta,
            preprocess_pre=preprocess_pre,
            centroids_pre=centroids_pre,
        )

    # -------------------- utilidades internas --------------------

    @staticmethod
    def _ensure_dataframe(df: Any) -> pd.DataFrame:
        """
        Asegura que la entrada se convierte en un DataFrame.

        - Si ya es DataFrame, se devuelve una copia.
        - Si es dict, se interpreta como un único registro (index = [0]).
        - Si es Series, se interpreta como una fila (index = [0]).
        """
        if isinstance(df, pd.DataFrame):
            return df.copy()
        elif isinstance(df, pd.Series):
            return df.to_frame().T.copy()
        elif isinstance(df, dict):
            return pd.DataFrame([df]).copy()
        else:
            raise TypeError(
                "PortablePredictor espera un DataFrame, Series o dict como entrada."
            )

    def _attach_baja_comp_dups_for_rf124(
        self,
        df: pd.DataFrame,
        baja_comp: pd.Series,
    ) -> pd.DataFrame:
        """
        Inserta 'baja_comp' y sus duplicadas en df para el RF_124, siguiendo
        la información de expected_raw_features del modelo portable.

        Estrategia:
          - Columna base: 'baja_comp' (si no existe, se crea).
          - Duplicadas: todas las columnas categóricas de RF_124 cuyo nombre
            empiece por 'baja_comp_dup'.
        """
        df2 = df.copy()

        # Base
        df2["baja_comp"] = baja_comp.astype(int)

        # Duplicadas según expected_raw_features del RF_124
        exp_124 = self.rf_124.artifact.get("feature_space", {}).get("expected_raw_features", {}) or {}
        cat_124 = list(exp_124.get("cat", []))
        dup_names = [c for c in cat_124 if c.startswith("baja_comp_dup")]

        for dn in dup_names:
            df2[dn] = df2["baja_comp"]

        return df2

    # -------------------- API pública: predicciones --------------------

    def predict(self, df_raw: Any) -> Dict[str, Any]:
        """
        Ejecuta toda la cadena de predicción:

          - XGB baja_comp -> baja_comp (0/1) + p_bajo
          - RF base       -> clases + probabilidades
          - RF_124        -> clases + probabilidades (usando baja_comp y duplicadas)
          - Ensemble      -> combinación RF base vs RF_124 + baja_comp

        Parámetros
        ----------
        df_raw : DataFrame, Series o dict
            df_norm con todas las columnas necesarias para los modelos.

        Devuelve
        --------
        dict con:
          - "rf_base" : {"pred": Series, "proba": DataFrame}
          - "rf_124"  : {"pred": Series, "proba": DataFrame}
          - "xgb_baja": {"baja_comp": Series, "p_bajo": Series, "threshold_bajo": float}
          - "ensemble": DataFrame con la predicción final y metadatos.
        """
        df = self._ensure_dataframe(df_raw)

        # 1) XGB baja_comp
        baja_comp, p_bajo = self.xgb_baja.predict_baja(df)

        # 2) RF base
        proba_base = self.rf_base.predict_proba(df)
        pred_base = self.rf_base.predict(df)

        # 3) RF_124 (requiere baja_comp + duplicadas)
        df_rf124 = self._attach_baja_comp_dups_for_rf124(df, baja_comp)
        proba_124 = self.rf_124.predict_proba(df_rf124)
        pred_124 = self.rf_124.predict(df_rf124)

        # 4) Ensemble
        ensemble_df = self.ensemble.apply(
            pred_base=pred_base,
            proba_base=proba_base,
            labels_base=self.rf_base.classes_,
            pred_124=pred_124,
            proba_124=proba_124,
            labels_124=self.rf_124.classes_,
            baja_comp=baja_comp,
            p_bajo=p_bajo,
        )

        # Construimos salidas en estructuras cómodas
        idx = df.index
        rf_base_out = {
            "pred": pd.Series(pred_base, index=idx, name="rf_base_pred"),
            "proba": pd.DataFrame(proba_base, index=idx, columns=self.rf_base.classes_),
        }
        rf_124_out = {
            "pred": pd.Series(pred_124, index=idx, name="rf_124_pred"),
            "proba": pd.DataFrame(proba_124, index=idx, columns=self.rf_124.classes_),
        }
        xgb_out = {
            "baja_comp": baja_comp,
            "p_bajo": p_bajo,
            "threshold_bajo": float(self.xgb_baja.threshold_bajo),
        }

        return {
            "rf_base": rf_base_out,
            "rf_124": rf_124_out,
            "xgb_baja": xgb_out,
            "ensemble": ensemble_df,
        }

    def knn_neighbors(
        self,
        df: Any,
        topk: int = 10,
        space: str = "all",
    ) -> Dict[str, Any]:
        """
        Calcula vecinos más cercanos en el espacio PRE_NUM a partir de df.

        Parámetros
        ----------
        df : DataFrame, Series o dict
            df_norm con, al menos, las columnas pre_num_features del kNN.
        topk : int
            Número máximo de vecinos a devolver.
        space : {"train","all"}
            Conjunto de referencia (Z_train o Z_all).

        Devuelve
        --------
        dict como el de KNNPortable.neighbors().
        """
        df2 = self._ensure_dataframe(df)
        return self.knn.neighbors(df2, topk=topk, space=space)


