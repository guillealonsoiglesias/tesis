class RandomForestPortable:
    """
    Implementación portable de un RandomForestClassifier a partir de su JSON.

    - Usa PortablePreprocessor para replicar el preprocesado.
    - Cada árbol se evalúa explícitamente sobre la matriz transformada.
    """

    def __init__(self, artifact: Dict[str, Any]) -> None:
        """
        Parámetros
        ----------
        artifact : dict
            Diccionario cargado desde 'rf_base_portable.json' o
            'rf_124_portable.json'. Debe contener, al menos:

            - 'model_type': "RandomForestClassifier"
            - 'classes', 'n_classes'
            - 'feature_space': {
                  'expected_raw_features': {'num': [...], 'cat': [...]},
                  'alias_mapping_used_in_train': {...},
                  'dtypes_expect': {...},
                  'preprocess': {...}
              }
            - 'forest': {
                  'n_estimators', 'max_depth', 'n_features_in', 'trees': [...]
              }
        """
        self.artifact = artifact
        if artifact.get("model_type") != "RandomForestClassifier":
            raise ValueError("Solo se soporta RandomForestClassifier en este portable.")

        # Clases y metadatos
        self.classes_: List[str] = [str(c) for c in artifact.get("classes", [])]
        self.n_classes_: int = int(artifact.get("n_classes", len(self.classes_)))

        # Espacio de características y preprocesador
        fs = artifact.get("feature_space", {}) or {}
        self.expected_raw_features: Dict[str, List[str]] = fs.get("expected_raw_features", {}) or {}
        self.alias_mapping: Dict[str, str] = fs.get("alias_mapping_used_in_train", {}) or {}
        self.dtypes_expect: Dict[str, str] = fs.get("dtypes_expect", {}) or {}
        prep_schema = fs.get("preprocess", {}) or {}
        self.preprocessor = PortablePreprocessor(prep_schema)

        # Bosque
        forest = artifact.get("forest", {}) or {}
        self.n_estimators: int = int(forest.get("n_estimators", 0))
        self.max_depth: Optional[int] = (
            int(forest["max_depth"]) if forest.get("max_depth") is not None else None
        )
        self.n_features_in_: int = int(forest.get("n_features_in", 0))

        self.trees: List[Dict[str, Any]] = list(forest.get("trees", []))
        if not self.trees:
            raise ValueError("El modelo RF portable no contiene árboles.")
        if self.n_classes_ <= 0:
            raise ValueError("El modelo RF portable no tiene clases definidas.")

    # -------------------- adaptador de entrada --------------------

    def _prepare_raw_input(self, df_raw: pd.DataFrame) -> pd.DataFrame:
        """
        Aplica alias de train y asegura que están todas las columnas
        de expected_raw_features.num y expected_raw_features.cat.

        - alias_mapping_used_in_train se interpreta como:
          {nombre_original_en_train: nombre_usado_en_modelo}
        """
        if not isinstance(df_raw, pd.DataFrame):
            raise TypeError("RandomForestPortable espera un pandas.DataFrame como entrada.")

        df = df_raw.copy()

        # 1) Aliases de train: preferred -> used
        #    alias_mapping_used_in_train viene típico como:
        #    {"Criterio_precio_p": "C_precio_p", ...}
        for preferred, used in self.alias_mapping.items():
            if used in df.columns:
                # Ya existe la columna con el nombre usado en train
                continue
            if preferred in df.columns:
                # Creamos la columna "used" copiando la "preferred"
                df[used] = df[preferred]

        cols_num = list(self.expected_raw_features.get("num", []))
        cols_cat = list(self.expected_raw_features.get("cat", []))
        ordered = cols_num + cols_cat

        # 2) Asegurar columnas y tipos mínimos
        for c in ordered:
            if c not in df.columns:
                # Columna esperada que no está: la creamos como NaN
                df[c] = np.nan

        # Numéricas a float
        for c in cols_num:
            df[c] = pd.to_numeric(df[c], errors="coerce")

        # Categóricas: no forzamos type, el PortablePreprocessor las tratará
        return df[ordered]

    # -------------------- evaluación de árboles --------------------

    def _tree_predict_proba(self, tree: Dict[str, Any], X: np.ndarray) -> np.ndarray:
        """
        Evalúa un único árbol sobre todas las filas de X y devuelve
        una matriz (n_samples, n_classes) con las probabilidades de ese árbol.
        """
        children_left = np.asarray(tree["children_left"], dtype=int)
        children_right = np.asarray(tree["children_right"], dtype=int)
        feature = np.asarray(tree["feature"], dtype=int)
        threshold = np.asarray(tree["threshold"], dtype=float)
        value = np.asarray(tree["value"], dtype=float)  # shape (n_nodes, n_classes)

        n_nodes = int(tree.get("n_nodes", len(children_left)))
        n_samples = X.shape[0]

        out = np.zeros((n_samples, self.n_classes_), dtype=float)

        for i in range(n_samples):
            node = 0
            # Descenso por el árbol
            while True:
                # Nodo fuera de rango: por robustez, volvemos a la raíz y salimos
                if node < 0 or node >= n_nodes:
                    node = 0
                    break

                # Consideramos hoja si no tiene hijos o si la feature es < 0
                if (
                    children_left[node] == -1
                    and children_right[node] == -1
                ) or feature[node] < 0:
                    break

                f = feature[node]
                thr = threshold[node]
                x_ij = X[i, f]

                if x_ij <= thr:
                    node = children_left[node]
                else:
                    node = children_right[node]

            if node < 0 or node >= n_nodes:
                # Nodo inválido: por robustez, usamos la raíz
                node = 0

            proba_node = value[node]
            # Re-normalizamos por seguridad
            s = proba_node.sum()
            if s <= 0.0:
                proba_node = np.ones(self.n_classes_, dtype=float) / float(self.n_classes_)
            else:
                proba_node = proba_node / s

            out[i, :] = proba_node

        return out

    # -------------------- API pública --------------------

    def predict_proba(self, df_raw: pd.DataFrame) -> np.ndarray:
        """
        Devuelve la matriz de probabilidades (n_samples, n_classes),
        promediando las predicciones de todos los árboles.
        """
        df_prep = self._prepare_raw_input(df_raw)
        X = self.preprocessor.transform(df_prep)
        X = _ensure_2d_array(X)

        if X.shape[1] != self.n_features_in_ and self.n_features_in_ > 0:
            # Aviso suave; no lanzamos error duro para permitir cierta tolerancia
            pass

        n_samples = X.shape[0]
        proba_acc = np.zeros((n_samples, self.n_classes_), dtype=float)

        for tree in self.trees:
            proba_acc += self._tree_predict_proba(tree, X)

        # Promedio de todos los árboles
        proba_acc /= float(len(self.trees))

        # Normalización final por seguridad
        sums = proba_acc.sum(axis=1, keepdims=True)
        sums[sums == 0.0] = 1.0
        proba_acc /= sums

        return proba_acc

    def predict(self, df_raw: pd.DataFrame) -> np.ndarray:
        """Devuelve las clases predichas (np.ndarray de str)."""
        proba = self.predict_proba(df_raw)
        idx = np.argmax(proba, axis=1)
        classes = np.array(self.classes_, dtype=object)
        return classes[idx]

