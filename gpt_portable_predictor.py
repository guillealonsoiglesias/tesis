# ---------------------------------------------------------------------------
# XGBBajaPortable
# ---------------------------------------------------------------------------

class XGBBajaPortable:
    """
    Implementación portable del modelo XGBoost multi-clase (BAJO/MEDIO/ALTO)
    para predecir baja_comp.

    - Objetivo: multi:softprob.
    - A partir de xgb_baja_portable.json + xgb_baja_model.json.
    """

    def __init__(self, meta: Dict[str, Any], booster: Dict[str, Any]) -> None:
        self.meta = meta
        self.variables_predictoras: List[str] = list(meta.get("variables_predictoras", []))
        self.encoders_schema: Dict[str, Dict[str, Any]] = meta.get("encoders_schema", {}) or {}
        self.labels: List[str] = list(meta.get("labels", [])) or ["BAJO", "MEDIO", "ALTO"]
        self.policies: Dict[str, Any] = meta.get("policies", {}) or {}
        self.fallback_class_por_col: Dict[str, str] = self.policies.get(
            "fallback_class_por_col", {}
        )

        # Umbral fijo para la clase BAJO, independientemente del meta
        # (decisión explícita para este script portable).
        self.threshold_bajo: float = 0.38

        # Booster JSON (estructura interna de XGBoost)
        learner = booster.get("learner", {}) or {}
        self.feature_names: List[str] = list(learner.get("feature_names", []))
        lm_param = learner.get("learner_model_param", {}) or {}
        self.base_score: float = float(lm_param.get("base_score", 0.5))
        self.num_class: int = int(lm_param.get("num_class", len(self.labels) or 3))

        gb = learner.get("gradient_booster", {}) or {}
        model = gb.get("model", {}) or {}
        self.trees: List[Dict[str, Any]] = list(model.get("trees", []))
        self.tree_info: List[int] = list(model.get("tree_info", []))

        if len(self.tree_info) != len(self.trees):
            raise ValueError("Inconsistencia entre número de árboles y tree_info en XGB.")

        # Índice de la clase BAJO
        try:
            self.idx_bajo: int = self.labels.index("BAJO")
        except ValueError:
            self.idx_bajo = 0

        # Preconstruimos esquemas de encoders: col -> {clase: índice}
        self._encoders_index: Dict[str, Dict[str, int]] = {}
        for col, schema in self.encoders_schema.items():
            classes = [str(c) for c in schema.get("classes", [])]
            self._encoders_index[col] = {cls: i for i, cls in enumerate(classes)}

    # -------------------- preprocesado de entrada --------------------

    def _prepare_X(self, df_raw: pd.DataFrame) -> pd.DataFrame:
        """
        Construye el DataFrame X con las variables_predictoras en el orden
        exacto esperado por el booster.
        """
        if not isinstance(df_raw, pd.DataFrame):
            raise TypeError("XGBBajaPortable espera un pandas.DataFrame como entrada.")

        X = pd.DataFrame(index=df_raw.index)

        # 1) Columnas básicas (num y cat ya codificados como en df_norm)
        for col in self.variables_predictoras:
            if col in df_raw.columns:
                X[col] = df_raw[col]
            else:
                # Política de columna numérica faltante: fill_0 (coherente con meta)
                X[col] = 0

        # 2) Encoders categóricos tipo LabelEncoder
        fallback_map = self.fallback_class_por_col or {}
        for col, idx_map in self._encoders_index.items():
            if col in X.columns:
                s = X[col].astype(str)
                classes = set(idx_map.keys())
                fb = str(fallback_map.get(col, next(iter(classes)) if classes else ""))

                # Sustituimos valores fuera del repertorio por el fallback
                s = s.where(s.isin(classes), other=fb)

                # Transformamos a índices
                def to_idx(val: Any) -> int:
                    try:
                        return idx_map[str(val)]
                    except KeyError:
                        return idx_map.get(fb, 0)

                X[col] = s.map(to_idx).astype(int)
            else:
                # Columna prevista pero ausente -> 0
                X[col] = 0

        # 3) Mes_lici a entero si está
        if "Mes_lici" in X.columns and not pd.api.types.is_integer_dtype(X["Mes_lici"]):
            X["Mes_lici"] = (
                pd.to_numeric(X["Mes_lici"], errors="coerce")
                .fillna(0)
                .astype(int)
            )

        return X[self.variables_predictoras]

    # -------------------- evaluación de árboles --------------------

    def _tree_predict_margin(self, tree: Dict[str, Any], X: np.ndarray) -> np.ndarray:
        """
        Evalúa un árbol XGBoost sobre X y devuelve los márgenes (una dimensión).
        """
        left_children = np.asarray(tree["left_children"], dtype=int)
        right_children = np.asarray(tree["right_children"], dtype=int)
        split_indices = np.asarray(tree["split_indices"], dtype=int)
        split_conditions = np.asarray(tree["split_conditions"], dtype=float)
        default_left = np.asarray(tree["default_left"], dtype=int)
        base_weights = np.asarray(tree["base_weights"], dtype=float)

        n_nodes = int(tree.get("tree_param", {}).get("num_nodes", len(left_children)))
        n_samples = X.shape[0]

        out = np.zeros(n_samples, dtype=float)

        for i in range(n_samples):
            node = 0
            while node >= 0 and node < n_nodes:
                left = left_children[node]
                right = right_children[node]

                # Hoja: no hay hijos
                if left == -1 and right == -1:
                    break

                feat_idx = split_indices[node]
                thr = split_conditions[node]
                v = X[i, feat_idx]

                if math.isnan(v):
                    # Usamos default_left
                    go_left = bool(default_left[node])
                else:
                    # Regla típica de XGBoost: v < thr -> izquierda
                    go_left = v < thr

                node = left if go_left else right
                if node == -1:
                    break

            if node == -1:
                # Por robustez, volvemos a la raíz
                node = 0

            out[i] = base_weights[node]

        return out

    # -------------------- API pública --------------------

    def predict_proba(self, df_raw: pd.DataFrame) -> np.ndarray:
        """
        Devuelve la matriz de probabilidades (n_samples, num_class)
        del XGBoost multi-clase (BAJO/MEDIO/ALTO).
        """
        X_df = self._prepare_X(df_raw)
        X = X_df.to_numpy(dtype=float)
        X = _ensure_2d_array(X)

        n_samples = X.shape[0]
        margins = np.full((n_samples, self.num_class), self.base_score, dtype=float)

        for t_idx, tree in enumerate(self.trees):
            class_id = int(self.tree_info[t_idx])
            if class_id < 0 or class_id >= self.num_class:
                continue
            contrib = self._tree_predict_margin(tree, X)
            margins[:, class_id] += contrib

        # Usamos el softmax fila a fila definido en las utilidades
        proba = _softmax(margins)
        return proba

    def predict_baja(self, df_raw: pd.DataFrame) -> Tuple[pd.Series, pd.Series]:
        """
        Devuelve:
          - baja_comp: Series de int (0/1)
          - p_bajo: Series de float (probabilidad de BAJO)
        """
        proba = self.predict_proba(df_raw)
        if proba.shape[1] <= self.idx_bajo:
            # Modelo mal formado; devolvemos todo ceros
            p_bajo = np.zeros(proba.shape[0], dtype=float)
        else:
            p_bajo = proba[:, self.idx_bajo]

        baja_flag = (p_bajo >= self.threshold_bajo).astype(np.int8)
        idx = df_raw.index
        return (
            pd.Series(baja_flag, index=idx, name="baja_comp"),
            pd.Series(p_bajo, index=idx, name="p_bajo"),
        )

