@dataclass
class ZipStore:
    """
    Pequeño contenedor para acceder a artefactos dentro de un ZIP.

    Uso típico:
        store = ZipStore("/ruta/a/modelos_portable.zip")
        meta_rf = store.read_json("rf_base_portable.json")
        Z_train = store.read_npz("knn_train.npz")["Z"]

    Internamente construye un índice de:
        nombre_corto -> ruta_completa_en_el_zip

    por ejemplo:
        "rf_base_portable.json" -> "modelos_portable/rf_base_portable.json"
    """

    zip_path: str

    def __post_init__(self) -> None:
        # Abrimos el ZIP una única vez y construimos un índice por nombre corto
        self._zf = zipfile.ZipFile(self.zip_path, mode="r")
        self._index: Dict[str, str] = {}

        for name in self._zf.namelist():
            # Ignoramos directorios
            if name.endswith("/"):
                continue
            short = name.split("/")[-1]
            if short in self._index:
                # Preferimos fallar pronto si hay colisiones de nombres cortos
                raise ValueError(
                    f"Nombre corto duplicado en el ZIP: {short!r} "
                    f"(rutas: {self._index[short]!r} y {name!r})"
                )
            self._index[short] = name

    # ---------------- Métodos públicos de utilidad ----------------

    def list(self) -> List[str]:
        """Devuelve la lista de nombres cortos disponibles en el ZIP."""
        return sorted(self._index.keys())

    def has(self, short_name: str) -> bool:
        """Indica si existe un fichero con ese nombre corto en el ZIP."""
        return short_name in self._index

    def _full_name(self, short_name: str) -> str:
        """Resuelve el nombre corto a la ruta completa dentro del ZIP."""
        try:
            return self._index[short_name]
        except KeyError:
            raise FileNotFoundError(
                f"No se encuentra {short_name!r} en el ZIP {self.zip_path!r}."
            )

    def read_json(self, short_name: str) -> Dict[str, Any]:
        """
        Lee y decodifica un JSON dentro del ZIP.

        Parámetros
        ----------
        short_name : str
            Nombre corto del fichero, por ejemplo 'rf_base_portable.json'.

        Devuelve
        --------
        dict
            Contenido del JSON parseado.
        """
        full = self._full_name(short_name)
        raw = self._zf.read(full)
        return json.loads(raw.decode("utf-8"))

    def read_npz(self, short_name: str) -> np.lib.npyio.NpzFile:
        """
        Lee un fichero .npz dentro del ZIP y devuelve el objeto NpzFile.

        Parámetros
        ----------
        short_name : str
            Nombre corto del fichero, por ejemplo 'knn_train.npz'.

        Devuelve
        --------
        np.lib.npyio.NpzFile
            Objeto desde el que se pueden leer arrays, p.ej. ['Z'].
        """
        from io import BytesIO

        full = self._full_name(short_name)
        raw = self._zf.read(full)
        bio = BytesIO(raw)
        return np.load(bio)

    def close(self) -> None:
        """Cierra el ZIP subyacente (opcional; no es estrictamente necesario)."""
        try:
            self._zf.close()
        except Exception:
            pass

    # Soporte para usarlo como contexto: with ZipStore(...) as store:
    def __enter__(self) -> "ZipStore":
        return self

    def __exit__(self, exc_type, exc_val, exc_tb) -> None:
        self.close()

