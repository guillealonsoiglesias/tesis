def generate_and_save_rag_case_document(
    predictor: PortablePredictor,
    df_raw: pd.DataFrame,
    row_index: int,
    knn_topk: int = 10,
    knn_space: str = "all",
    output_path: str = "rag_case_doc.json",
) -> Dict[str, Any]:
    """
    Genera el documento RAG completo para un expediente y lo guarda en JSON.

    Este será el archivo 'legible y utilizable' por el GPT:
      - Se puede cargar con json.load(...)
      - El GPT lo usará como documento fuente en el RAG, sin necesidad de
        acceder a df_norm ni a otros artefactos de kNN.
    """
    rag_doc = build_rag_case_document(
        predictor=predictor,
        df_raw=df_raw,
        row_index=row_index,
        knn_topk=knn_topk,
        knn_space=knn_space,
    )

    output_path = Path(output_path)
    with output_path.open("w", encoding="utf-8") as f:
        json.dump(rag_doc, f, ensure_ascii=False, indent=2)

    print(f"Documento RAG guardado en {output_path.resolve()}")
    return rag_doc