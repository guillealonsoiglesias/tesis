class PortablePreprocessor:
    """
    Preprocesador portable que reproduce:
      - SimpleImputer (num) + StandardScaler
      - SimpleImputer (cat) + OneHotEncoder(handle_unknown="ignore")

    El esquema debe tener la forma usada en:
      - preprocess_pre_portable.json
      - feature_space["preprocess"] de los RF portables.
    """

    def __init__(self, schema: Dict[str, Any]) -> None:
        self.schema = schema

        num = schema.get("numeric", {}) or {}
        cat = schema.get("categorical", {}) or {}

        # ---------------- Configuración numérica ----------------
        self.num_cols: List[str] = list(num.get("columns", []))
        num_imp = num.get("imputer", {}) or {}
        num_scl = num.get("scaler", {}) or {}

        self.num_imp_strategy: str = num_imp.get("strategy", "median")
        self.num_imp_statistics: np.ndarray = np.asarray(
            num_imp.get("statistics", []), dtype=float
        )

        self.num_with_mean: bool = bool(num_scl.get("with_mean", True))
        self.num_with_std: bool = bool(num_scl.get("with_std", True))
        self.num_mean: np.ndarray = np.asarray(
            num_scl.get("mean", np.zeros(len(self.num_cols))), dtype=float
        )
        self.num_scale: np.ndarray = np.asarray(
            num_scl.get("scale", np.ones(len(self.num_cols))), dtype=float
        )
        # Evitar divisiones por cero más adelante
        self.num_scale[self.num_scale == 0.0] = 1.0

        # ---------------- Configuración categórica ----------------
        self.cat_cols: List[str] = list(cat.get("columns", []))
        cat_imp = cat.get("imputer", {}) or {}
        cat_oh = cat.get("onehot", {}) or {}

        self.cat_imp_strategy: str = cat_imp.get("strategy", "most_frequent")
        self.cat_fill_values: List[Any] = list(cat_imp.get("fill_values", []))
        self.cat_handle_unknown: str = cat_oh.get("handle_unknown", "ignore")

        # categories: dict col -> list[str]
        self.cat_categories: Dict[str, List[str]] = cat_oh.get("categories", {}) or {}

        # Nombres de salida opcionales (no imprescindibles para la inferencia)
        self.feature_names_out: Optional[List[str]] = schema.get("feature_names_out")

    # -------------------- numéricos --------------------

    def _transform_numeric(self, df: pd.DataFrame) -> np.ndarray:
        if not self.num_cols:
            return np.zeros((len(df), 0), dtype=float)

        X_num = pd.DataFrame(index=df.index)
        for c in self.num_cols:
            if c in df.columns:
                X_num[c] = pd.to_numeric(df[c], errors="coerce")
            else:
                # Columna ausente: todo NaN
                X_num[c] = np.nan

        A = X_num.to_numpy(dtype=float)  # shape (n_samples, n_features)

        # Imputación: implementamos 'median'/'most_frequent' vía statistics
        if self.num_imp_strategy in ("median", "most_frequent"):
            stats = self.num_imp_statistics
            if stats.shape[0] != A.shape[1]:
                raise ValueError(
                    "Dimensión de estadísticas numéricas inconsistente: "
                    f"{stats.shape[0]} != {A.shape[1]}"
                )
            # Imputamos NaN columna a columna
            for j in range(A.shape[1]):
                col = A[:, j]
                mask = np.isnan(col)
                if mask.any():
                    col[mask] = stats[j]
            # A ya modificado in-place

        # Escalado estándar
        if self.num_with_mean:
            A = A - self.num_mean
        if self.num_with_std:
            A = A / self.num_scale

        return A

    # -------------------- categóricas --------------------

    def _normalize_categorical_values(self, col: str, s: pd.Series) -> pd.Series:
        """
        Normaliza los valores de una columna categórica:

        - Si todas las categorías de esquema son enteros representados como str
          ("1","2",...), convierte los valores numéricos a int y luego a str
          (así 1, 1.0 y "1" se mapean a "1").
        - En otro caso, convierte a str tal cual.
        """
        cats = self.cat_categories.get(col, [])
        if cats and all(_is_int_string(c) for c in cats):
            # Columna con categorías "numéricas"
            def to_int_str(x: Any) -> Any:
                if pd.isna(x):
                    return np.nan  # se imputará antes
                sx = str(x).strip()
                try:
                    # Permite valores tipo "1.0"
                    val = int(float(sx))
                    return str(val)
                except Exception:
                    return sx  # fallback: lo que sea como str

            return s.map(to_int_str)
        else:
            # Columna categórica convencional
            return s.astype(str)

    def _transform_categorical(self, df: pd.DataFrame) -> np.ndarray:
        if not self.cat_cols:
            return np.zeros((len(df), 0), dtype=float)

        n = len(df)
        blocks: List[np.ndarray] = []

        for idx, col in enumerate(self.cat_cols):
            if col in df.columns:
                s = df[col]
            else:
                # Columna ausente: todo NaN
                s = pd.Series([np.nan] * n, index=df.index)

            # Imputación
            if self.cat_imp_strategy == "most_frequent":
                # fill_values está en el mismo orden que cat_cols
                if idx < len(self.cat_fill_values):
                    fill_val = self.cat_fill_values[idx]
                else:
                    # Fallback defensivo
                    fill_val = (
                        s.mode(dropna=True).iloc[0] if s.notna().any() else ""
                    )
                s = s.fillna(fill_val)
            else:
                # Otras estrategias no se contemplan en tus artefactos
                s = s.fillna("")

            # Normalización de valores (numéricos como enteros en str)
            s_norm = self._normalize_categorical_values(col, s)

            # One-hot
            cats = self.cat_categories.get(col, [])
            cats = [str(c) for c in cats]

            # Matriz (n_samples, len(cats))
            M = np.zeros((n, len(cats)), dtype=float)
            if len(cats) == 0:
                blocks.append(M)
                continue

            # Mapeo valor->índice de categoría
            cat_to_idx = {str(c): j for j, c in enumerate(cats)}

            # Para cada fila, activamos la posición correspondiente
            for i, val in enumerate(s_norm):
                if pd.isna(val):
                    # Ya imputado, pero por seguridad
                    continue
                key = str(val)
                j = cat_to_idx.get(key, None)
                if j is None:
                    # handle_unknown:
                    if self.cat_handle_unknown == "ignore":
                        continue
                    else:
                        # En tus modelos, esto no debería ocurrir; lanzamos error explícito
                        raise ValueError(
                            f"Valor desconocido '{key}' en columna '{col}' "
                            f"con handle_unknown='{self.cat_handle_unknown}'"
                        )
                M[i, j] = 1.0

            blocks.append(M)

        if blocks:
            return np.concatenate(blocks, axis=1)
        else:
            return np.zeros((len(df), 0), dtype=float)

    # -------------------- API pública --------------------

    def transform(self, df: pd.DataFrame) -> np.ndarray:
        """
        Aplica el preprocesado numérico + categórico y devuelve
        una matriz NumPy 2D (n_samples, n_features_transformados).
        """
        if not isinstance(df, pd.DataFrame):
            raise TypeError("PortablePreprocessor.transform espera un pandas.DataFrame.")

        A_num = self._transform_numeric(df)
        A_cat = self._transform_categorical(df)

        if A_num.shape[0] != A_cat.shape[0]:
            raise ValueError("Dimensiones numéricas y categóricas incompatibles.")

        if A_num.shape[1] == 0 and A_cat.shape[1] == 0:
            return np.zeros((len(df), 0), dtype=float)

        return np.hstack([A_num, A_cat])

